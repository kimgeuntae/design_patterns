
public class Math {

	// 두배 | *2
	public static double twoTime(double num) {
		return num*2;
	}
	
	// 절반 | /2
	public static double half(double num) {
//		System.out.println("start halfof()");
		// 여기에 로그를 찍으면 Math 라이브러리를 사용하는 모든 곳에서 로그가 찍히니까 Impl 에 로그 호출
		
		return num/2;
	}
	
	// 강화된 알고리즘 | 변결 될 알고리즘
	public static Double triple(double d) {
		return d*3;
	}
}
